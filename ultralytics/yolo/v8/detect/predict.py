def parse_class_filter(value):
    if value is None:
        return None
    if isinstance(value, str):
        value = value.strip()
        if value.lower() in ("", "none", "null", "all"):
            return None
        value = value.strip("[]()")
        return [int(v.strip()) for v in value.split(",") if v.strip()]
    try:
        return [int(v) for v in value]
    except TypeError:
        pass
    return [int(value)]


def filter_detections_by_class(det, class_filter):
    if class_filter is None or len(det) == 0:
        return det
    allowed = torch.tensor(class_filter, device=det.device, dtype=torch.long)
    cls = det[:, 5].long()
    return det[(cls[:, None] == allowed[None, :]).any(dim=1)]


